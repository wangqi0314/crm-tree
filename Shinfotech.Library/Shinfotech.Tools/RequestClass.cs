        /// <param name="strName"></param>
        /// <returns></returns>
        public static DateTime GetFormDateTime(string strName)
        {
            DateTime time = Convert.ToDateTime("1900-1-1 " + DateTime.Now.ToString("HH:mm:ss"));
            if (HttpContext.Current.Request.Form[strName] == null)
            {
                return time;
            }
            else
            {
                try
                {
                    return time = Convert.ToDateTime(HttpContext.Current.Request.Form[strName].Trim() + " " + DateTime.Now.ToString("HH:mm:ss"));
                }
                catch
                {
                    return time;
                }
            }

        }
